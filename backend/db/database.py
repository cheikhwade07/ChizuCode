SCHEMA_SQL = """
-- Enable pgvector extension
CREATE EXTENSION IF NOT EXISTS vector;

-- repos: one row per ingested GitHub repository
CREATE TABLE IF NOT EXISTS repos (
    id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    github_url  TEXT NOT NULL UNIQUE,
    name        TEXT,                    -- owner/repo extracted from URL
    status      TEXT NOT NULL DEFAULT 'pending',
                                         -- pending | ingesting | ready | failed
    phase       TEXT NOT NULL DEFAULT 'pending',
                                         -- current ingestion phase for polling UI
    error       TEXT,                    -- error message if status=failed
    chunk_count INT DEFAULT 0,
    cluster_tree JSONB,
    created_at  TIMESTAMPTZ DEFAULT now(),
    updated_at  TIMESTAMPTZ DEFAULT now()
);

-- chunks: one row per code/doc/config chunk
CREATE TABLE IF NOT EXISTS chunks (
    id             UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    repo_id        UUID NOT NULL REFERENCES repos(id) ON DELETE CASCADE,
    file_path      TEXT NOT NULL,
    file_type      TEXT NOT NULL,        -- code | config | doc
    language       TEXT,                 -- python | typescript | go | etc.
    raw_code       TEXT NOT NULL,
    summary        TEXT,                 -- generated by Gemini
    start_line     INT,
    end_line       INT,
    loc            INT,
    is_split       BOOLEAN DEFAULT FALSE,
    chunk_index    INT DEFAULT 0,
    created_at     TIMESTAMPTZ DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_chunks_repo_id ON chunks(repo_id);
CREATE INDEX IF NOT EXISTS idx_chunks_file_path ON chunks(repo_id, file_path);

-- vectors: one row per chunk, holds both embedding vectors
CREATE TABLE IF NOT EXISTS vectors (
    id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    chunk_id        UUID NOT NULL REFERENCES chunks(id) ON DELETE CASCADE,
    repo_id         UUID NOT NULL,
    domain_id       UUID,                -- set after clustering
    summary_vector  vector(1536),         -- Gemini embedding-001
    code_vector     vector(1536),        -- Voyage voyage-code-2
    created_at      TIMESTAMPTZ DEFAULT now()
);

-- HNSW indexes for fast approximate nearest neighbour search
CREATE INDEX IF NOT EXISTS idx_summary_vector
    ON vectors USING hnsw (summary_vector vector_cosine_ops);

CREATE INDEX IF NOT EXISTS idx_code_vector
    ON vectors USING hnsw (code_vector vector_cosine_ops);

CREATE INDEX IF NOT EXISTS idx_vectors_repo_id   ON vectors(repo_id);
CREATE INDEX IF NOT EXISTS idx_vectors_domain_id ON vectors(domain_id);

-- domains: recursive cluster hierarchy
CREATE TABLE IF NOT EXISTS domains (
    id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    repo_id     UUID NOT NULL REFERENCES repos(id) ON DELETE CASCADE,
    parent_id   UUID REFERENCES domains(id) ON DELETE CASCADE,
    label       TEXT NOT NULL,           -- e.g. "Authentication"
    summary     TEXT,                    -- one-line description from Gemini
    level       INT NOT NULL DEFAULT 0,  -- 0 = top level, 1 = submap, etc.
    chunk_ids   UUID[],                  -- chunks that belong to this domain
    created_at  TIMESTAMPTZ DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_domains_repo_id   ON domains(repo_id);
CREATE INDEX IF NOT EXISTS idx_domains_parent_id ON domains(parent_id);

-- repo_ingest_log: rolling IP quota for public demo usage
CREATE TABLE IF NOT EXISTS repo_ingest_log (
    id          SERIAL PRIMARY KEY,
    ip_address  TEXT        NOT NULL,
    repo_id     TEXT        NOT NULL,
    ingested_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_ingest_log_ip_day
    ON repo_ingest_log (ip_address, ingested_at);

ALTER TABLE repos ADD COLUMN IF NOT EXISTS phase TEXT NOT NULL DEFAULT 'pending';
"""


def create_schema() -> None:
    """Run schema creation SQL — safe to call multiple times (IF NOT EXISTS)."""
    with get_db(register_vectors=False) as conn:
        with conn.cursor() as cur:
            cur.execute(SCHEMA_SQL)
    logger.info("schema created / verified")


