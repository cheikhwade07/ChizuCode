def insert_chunks_batch(repo_id: str, chunks: list[dict]) -> list[str]:
    """
    Insert many chunks in one round trip.
    Returns list of UUIDs in the same order as input.
    """
    with get_db() as conn:
        with conn.cursor() as cur:
            records = [
                (
                    repo_id,
                    c["file_path"], c["file_type"], c.get("language"),
                    c["content"], c.get("summary"),
                    c.get("start_line"), c.get("end_line"),
                    c.get("loc"), c.get("is_split", False), c.get("chunk_index", 0),
                )
                for c in chunks
            ]
            psycopg2.extras.execute_values(
                cur,
                """
                INSERT INTO chunks (
                    repo_id, file_path, file_type, language,
                    raw_code, summary, start_line, end_line,
                    loc, is_split, chunk_index
                ) VALUES %s RETURNING id
                """,
                records,
            )
            return [str(row[0]) for row in cur.fetchall()]


