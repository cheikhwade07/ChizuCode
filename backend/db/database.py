def get_domain_tree(repo_id: str) -> list[dict]:
    """
    Fetch the full domain hierarchy for a repo as a flat list.
    Frontend builds the nested tree from parent_id references.
    """
    with get_db() as conn:
        with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
            cur.execute(
                """
                SELECT id, parent_id, label, summary, level, chunk_ids
                FROM domains
                WHERE repo_id = %s
                ORDER BY level, label
                """,
                (repo_id,),
            )
            return [dict(r) for r in cur.fetchall()]


