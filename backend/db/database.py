def search_summary_vectors(
    query_vector: list[float],
    repo_id: str,
    domain_id: str | None = None,
    top_k: int = 5,
) -> list[dict]:
    """
    Find top-k chunks by summary_vector cosine similarity.
    Optionally scoped to a specific domain.
    Returns list of dicts with chunk_id, file_path, summary, score.
    """
    with get_db() as conn:
        with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
            if domain_id:
                cur.execute(
                    """
                    WITH RECURSIVE scoped_domains AS (
                        SELECT id
                        FROM domains
                        WHERE id = %s::uuid AND repo_id = %s::uuid
                        UNION ALL
                        SELECT d.id
                        FROM domains d
                        JOIN scoped_domains sd ON d.parent_id = sd.id
                        WHERE d.repo_id = %s::uuid
                    )
                    SELECT
                        c.id AS chunk_id,
                        c.file_path,
                        c.summary,
                        c.raw_code,
                        v.domain_id,
                        1 - (v.summary_vector <=> %s::vector) AS score
                    FROM vectors v
                    JOIN chunks c ON c.id = v.chunk_id
                    WHERE v.repo_id = %s
                      AND v.domain_id IN (SELECT id FROM scoped_domains)
                      AND v.summary_vector IS NOT NULL
                    ORDER BY v.summary_vector <=> %s::vector
                    LIMIT %s
                    """,
                    (domain_id, repo_id, repo_id, query_vector, repo_id, query_vector, top_k),
                )
            else:
                cur.execute(
                    """
                    SELECT
                        c.id AS chunk_id,
                        c.file_path,
                        c.summary,
                        c.raw_code,
                        v.domain_id,
                        1 - (v.summary_vector <=> %s::vector) AS score
                    FROM vectors v
                    JOIN chunks c ON c.id = v.chunk_id
                    WHERE v.repo_id = %s
                      AND v.summary_vector IS NOT NULL
                    ORDER BY v.summary_vector <=> %s::vector
                    LIMIT %s
                    """,
                    (query_vector, repo_id, query_vector, top_k),
                )
            return [dict(r) for r in cur.fetchall()]


