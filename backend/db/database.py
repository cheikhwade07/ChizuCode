def reset_repo_for_reingest(repo_id: str) -> None:
    with get_db() as conn:
        with conn.cursor() as cur:
            cur.execute("DELETE FROM domains WHERE repo_id = %s", (repo_id,))
            cur.execute("DELETE FROM chunks WHERE repo_id = %s", (repo_id,))
            cur.execute(
                """
                UPDATE repos
                SET status = 'pending',
                    phase = 'pending',
                    error = NULL,
                    chunk_count = 0,
                    cluster_tree = NULL,
                    updated_at = now()
                WHERE id = %s
                """,
                (repo_id,),
            )


